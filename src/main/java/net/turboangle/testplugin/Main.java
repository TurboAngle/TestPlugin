package net.turboangle.testplugin; // このファイルが置いてあるパソコン内の「住所」

import org.bukkit.plugin.java.JavaPlugin; // マイクラ公式が用意してくれた「プラグインの素」を読み込む

// =========================================================================
// 【重要】public final class Main extends JavaPlugin のバラバラ解説
// =========================================================================
// 🔓 public:   マイクラサーバー（外部）から、このファイルをいつでも自由に開いていいよという「公開宣言」。
// 🔒 final:    このメインクラスは大元（最終決定版）だから、これ以上コピーして改造しちゃダメという「鍵」。
// 📄 class:    Javaのプログラムの「設計図」という意味。
// 🤝 extends:  【継承（けいしょう）】マイクラ公式が作った「JavaPlugin」の機能を丸ごと引き継ぐ魔法の言葉。
//              これのおかげで、1から難しいコードを書かなくてもプラグインとして動くようになる！
// =========================================================================
public final class Main extends JavaPlugin {

    // 🔄 @Override: 元々マイクラ側で決まっている動きを、自分たちのオリジナル処理で「上書きするよ」という合図。
    @Override
    public void onEnable() {
        // 🚀 サーバーが起動して、このプラグインが読み込まれた瞬間に「1回だけ」自動で動く場所。
        // 主に：データの読み込み、コマンドの登録、起動メッセージの送信などを行う。

        // サーバーの黒い画面（コンソール）に、無事に起動したことをお知らせする
        getLogger().info("Test has been successfully enabled!");
    }

    // 🔄 @Override: こっちも元の動きを自分たちの処理で「上書きするよ」という合図。
    @Override
    public void onDisable() {
        // 🛑 サーバーが止まるとき（/stopしたときなど）に「1回だけ」自動で動く場所。
        // 主に：プレイヤーのデータを保存する、開いていたファイルを安全に閉じるなどの「後片付け」を行う。

        // サーバーの黒い画面（コンソール）に、安全に終了したことを報告する
        getLogger().info("Test Plugin has been safely disabled!");
    }
}